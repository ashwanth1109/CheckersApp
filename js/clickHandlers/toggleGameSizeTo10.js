//------------------------------------------------------------------------------------
// Function to toggle game size to 10
//------------------------------------------------------------------------------------
const toggleGameSizeTo10 = () => {
  if (!$(`#game-size-10`).hasClass(`button-selected`)) {
    $(`#game-size-10`).addClass(`button-selected`);
    $(`#game-size-8`).removeClass(`button-selected`);
  }

  //------------------------------------------------------------------------------------
  // Update game size to 10
  //------------------------------------------------------------------------------------
  gameSize = 10;

  //------------------------------------------------------------------------------------
  // Start a new game
  //------------------------------------------------------------------------------------
  startNewGame();
};
