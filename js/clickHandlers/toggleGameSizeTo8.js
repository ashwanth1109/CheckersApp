//------------------------------------------------------------------------------------
// Function to toggle game size to 8
//------------------------------------------------------------------------------------
const toggleGameSizeTo8 = () => {
  if (!$(`#game-size-8`).hasClass(`button-selected`)) {
    $(`#game-size-8`).addClass(`button-selected`);
    $(`#game-size-10`).removeClass(`button-selected`);
  }

  //------------------------------------------------------------------------------------
  // Update game size to 8
  //------------------------------------------------------------------------------------
  gameSize = 8;

  //------------------------------------------------------------------------------------
  // Start a new game
  //------------------------------------------------------------------------------------
  startNewGame();
};
