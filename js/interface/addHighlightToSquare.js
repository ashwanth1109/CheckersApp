//------------------------------------------------------------------------------------
// Highlight squares that can be moved to
//------------------------------------------------------------------------------------
const addHighlightToSquare = $square => {
  //------------------------------------------------------------------------------------
  // If square exists, ie. it is not null
  //------------------------------------------------------------------------------------
  if ($square) {
    //------------------------------------------------------------------------------------
    // then add highlightSquare class and remove dark class
    //------------------------------------------------------------------------------------
    $square.removeClass(`dark`).addClass(`highlightSquare`);
  }
};
