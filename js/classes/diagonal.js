//------------------------------------------------------------------------------------
// Diagonal Class
//------------------------------------------------------------------------------------

class Diagonal {
  //------------------------------------------------------------------------------------
  // Constructor function
  //------------------------------------------------------------------------------------
  constructor() {
    //------------------------------------------------------------------------------------
    // Each diagonal has a move position and jump position, initialized to null
    //------------------------------------------------------------------------------------
    this.movePosition = null;
    this.jumpPosition = null;
    //------------------------------------------------------------------------------------
    // If there is an opponent checker on move square, that is stored in diagonal object
    //------------------------------------------------------------------------------------
    this.opponentChecker = null;
  }
}
