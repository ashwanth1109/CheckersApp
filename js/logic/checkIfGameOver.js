//------------------------------------------------------------------------------------
// Function to check if game is over
//------------------------------------------------------------------------------------
const checkIfGameOver = () => {
  //------------------------------------------------------------------------------------
  // Check for win condition 1
  //------------------------------------------------------------------------------------
  //------------------------------------------------------------------------------------
  // Win score for 8 by 8 board is 12
  //------------------------------------------------------------------------------------
  let winScore = 12;
  if (gameSize === 10) {
    //------------------------------------------------------------------------------------
    // Win score for 10 by 10 board is 20
    //------------------------------------------------------------------------------------
    winScore = 20;
  }
  //------------------------------------------------------------------------------------
  // If black score has reached win score, then win condition is achieved
  //------------------------------------------------------------------------------------
  if (blackScore === winScore) {
    //------------------------------------------------------------------------------------
    // Call end game function passing in player 1 as winner
    //------------------------------------------------------------------------------------
    endGame(1);
  }
  //------------------------------------------------------------------------------------
  // If black score has reached win score, then win condition is achieved
  //------------------------------------------------------------------------------------
  else if (redScore === winScore) {
    //------------------------------------------------------------------------------------
    // Call end game function passing in player 2 as winner
    //------------------------------------------------------------------------------------
    endGame(2);
  }
};
