//------------------------------------------------------------------------------------
// Function to check if checker has reached the opponent edge and if it has
// then we convert it into a king piece that can move on all 4 diagonals
//------------------------------------------------------------------------------------
const checkIfAtOpponentEdge = $square => {
  //------------------------------------------------------------------------------------
  // Check if its player 1
  //------------------------------------------------------------------------------------
  if (currentPlayer === 1) {
    //------------------------------------------------------------------------------------
    // Check if square is on the first row
    //------------------------------------------------------------------------------------
    if ($square.data(`data`).row === 1) {
      //------------------------------------------------------------------------------------
      // Convert your checker to king piece -
      //------------------------------------------------------------------------------------
      crownYourChecker($square);
    }
  }
  //------------------------------------------------------------------------------------
  // Check if its player 1
  //------------------------------------------------------------------------------------
  else if (currentPlayer === 2) {
    //------------------------------------------------------------------------------------
    // Check if square is on the last row
    //------------------------------------------------------------------------------------
    if ($square.data(`data`).row === gameSize) {
      //------------------------------------------------------------------------------------
      // Convert your checker to king piece -
      //------------------------------------------------------------------------------------
      crownYourChecker($square);
    }
  }
};
